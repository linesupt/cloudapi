public class NutritionHealthAppraiseInfo {
    private String id;
    private String health_mode;
    private String show;
    private String light;
    private String appraise;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getHealth_mode() {
        return health_mode;
    }

    public void setHealth_mode(String health_mode) {
        this.health_mode = health_mode;
    }

    public String getShow() {
        return show;
    }

    public void setShow(String show) {
        this.show = show;
    }

    public String getLight() {
        return light;
    }

    public void setLight(String light) {
        this.light = light;
    }

    public String getAppraise() {
        return appraise;
    }

    public void setAppraise(String appraise) {
        this.appraise = appraise;
    }

}
